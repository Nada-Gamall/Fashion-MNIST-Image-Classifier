import tensorflow as tf
import matplotlib.pyplot as plt
import numpy as np
from tensorflow import keras

"""# Load Dataset --> keras has some built-in datasets"""

fashion_mnist = tf.keras.datasets.fashion_mnist

"""
train_images -->x_train
train_labels -->y_train
test_images -->x_test
test_labels -->y_test
"""
(train_images, train_labels), (test_images, test_labels) = fashion_mnist.load_data()

"""# print unique classes in the Dataset"""

print(np.unique(train_labels))

"""# print Dimension of train & test data"""

print(train_images.shape)
print(test_images.shape)

"""## print the first image
its a list of numbers for each each pixel and each between **0(black) --> 255(white)** becouse its in gray scale format
"""

train_images[0]

"""# Draw first images in a plot"""

plt.imshow(train_images[0],cmap='gray')
plt.colorbar()

"""# Data Preprocessing"""

# normalization(Scalling of data)--> divide all pixels by highest pixel which is 255.0
train_images = train_images / 255.0
test_images = test_images / 255.0

"""# print first image after normalization"""

train_images[0]

"""# Showing some samples of data set using Figures and subplot"""

#needed for naming each sample
class_names = ['T-shirt/top', 'Trouser', 'Pullover', 'Dress', 'Coat',
               'Sandal', 'Shirt', 'Sneaker', 'Bag', 'Ankle boot']

#create a simple figure with 10*10 inches width and height
plt.figure(figsize=(10,10))
for i in range(20):
    plt.subplot(5,5,i+1)
    #only remove numbers on both rows and columns
    plt.xticks([])
    plt.yticks([])
    #**************************
    plt.imshow(train_images[i], cmap='gray')
    plt.xlabel(class_names[train_labels[i]])
plt.show()

"""# Neural Network Model"""

model = keras.Sequential([
    keras.layers.Flatten(input_shape=(28, 28)),
    keras.layers.Dense(128, activation=tf.nn.relu),
    keras.layers.Dense(10, activation=tf.nn.softmax)
])

"""# Model Compilation"""

model.compile(optimizer='adam',
              loss='sparse_categorical_crossentropy', # diff between actual and probabilities
              metrics=['accuracy'])

"""# Train the model"""

model.fit(train_images, train_labels, epochs=10) # less epochs means UF , more epochs means OF

"""# Model Evaluation"""

test_loss, test_acc = model.evaluate(test_images, test_labels)

print('Test accuracy:', test_acc)

predictions = model.predict(test_images)

print('Probabilities of each class -->',predictions[20])
#print index that has highest probability
print('predicted class-->',np.argmax(predictions[20]))
print('true class-->',test_labels[20])



